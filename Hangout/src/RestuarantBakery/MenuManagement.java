public void showItemToFields(int index){
    lblId.setText("ID");
    lblMenuID.setText(Integer.toString(retrieveData().get(index).getId()));
    repaint();
    txtName.setText(retrieveData().get(index).getName());
    txtDes.setText(retrieveData().get(index).getDescription());
    txtPrice.setText(Float.toString(retrieveData().get(index).getPrice()));
}     
