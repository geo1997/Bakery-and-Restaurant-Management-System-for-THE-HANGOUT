    //retrieve data from the 
     public ArrayList<MenuClass>retrieveData(){
        ArrayList<MenuClass> m1=null;
        m1=new ArrayList<MenuClass>();
        try{
            Connection conn=MySqlConnection();
            String q="select menuId,menu_name,menu_description,price from menu ";
            Statement st=conn.createStatement();
            ResultSet rs=st.executeQuery(q);
            MenuClass menu;
            
            while(rs.next()){
                menu=new MenuClass(rs.getInt("menuId"),rs.getString("menu_name"),
                        rs.getString("menu_description"),
                        Float.parseFloat(rs.getString(4)));
                m1.add(menu);
                 
            }
            
        }
        catch(Exception e){
            System.out.println("Error RetrieveData"+e);
        
        }
        return m1;
    }
     
     //fill the table
    
    public void fillTable(){
    
        ArrayList<MenuClass> m1;
        m1 = retrieveData();
        DefaultTableModel model=(DefaultTableModel)tblMenu.getModel();
        model.setRowCount(0);
        
        Object[] row=new Object[4];
        for(int i =0;i<m1.size();i++){
            row[0]=m1.get(i).getId();
            row[1]=m1.get(i).getName();
            row[2]=m1.get(i).getDescription();
            row[3]=m1.get(i).getPrice();
            model.addRow(row);
            
        }
    }
    
