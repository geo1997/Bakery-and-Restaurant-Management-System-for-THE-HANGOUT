    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:

        String PATTERN="^[a-zA-Z ]{0,30}$";
        Pattern patt=Pattern.compile(PATTERN);
        Matcher match=patt.matcher(txtSearch.getText());

        if(!match.matches()){

            errlbl1.setText("*Enter valid menu name to search!");
            repaint();
        }
        else{
            errlbl1.setText("");
            repaint();
            ArrayList<MenuClass> m1=null;
            m1=new ArrayList<MenuClass>();
            String val=txtSearch.getText().toString();
            try {
                Connection conn=MySqlConnection();
                String qry="select * from menu where menu_name like '%"+val+"%'";
                Statement st=conn.createStatement();
                ResultSet rs=st.executeQuery(qry);
                MenuClass menu;
                while (rs.next()) {
                    menu=new MenuClass(rs.getInt("menuId"),rs.getString("menu_name"),
                        rs.getString("menu_description"),
                        Float.parseFloat(rs.getString(4)));
                    m1.add(menu);
                }

                DefaultTableModel model=(DefaultTableModel)tblMenu.getModel();
                model.setRowCount(0);

                Object[] row=new Object[4];
                for(int i =0;i<m1.size();i++){
                    row[0]=m1.get(i).getId();
                    row[1]=m1.get(i).getName();
                    row[2]=m1.get(i).getDescription();
                    row[3]=m1.get(i).getPrice();
                    model.addRow(row);
                }
            } catch (Exception e) {
                System.out.println(e);
            }

        }

    }//GEN-LAST:event_txtSearchKeyReleased

