    public void fillTable(){
    
        ArrayList<MenuClass> m1;
        m1 = retrieveData();
        DefaultTableModel model=(DefaultTableModel)tblMenu.getModel();
        model.setRowCount(0);
        
        Object[] row=new Object[4];
        for(int i =0;i<m1.size();i++){
            row[0]=m1.get(i).getId();
            row[1]=m1.get(i).getName();
            row[2]=m1.get(i).getDescription();
            row[3]=m1.get(i).getPrice();
            model.addRow(row);
            
        }
    }

