     public ArrayList<MenuClass>retrieveData(){
        ArrayList<MenuClass> m1=null;
        m1=new ArrayList<MenuClass>();
        try{
            Connection conn=MySqlConnection();
            String q="select menuId,menu_name,menu_description,price from menu ";
            Statement st=conn.createStatement();
            ResultSet rs=st.executeQuery(q);
            MenuClass menu;
            
            while(rs.next()){
                menu=new MenuClass(rs.getInt("menuId"),rs.getString("menu_name"),
                        rs.getString("menu_description"),
                        Float.parseFloat(rs.getString(4)));
                m1.add(menu);
                 
            }
            
        }
        catch(Exception e){
            System.out.println("Error RetrieveData"+e);
        
        }
        return m1;
    }
