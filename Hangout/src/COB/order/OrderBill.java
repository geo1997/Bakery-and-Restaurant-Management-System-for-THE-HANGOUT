        // TODO add your handling code here:
        String tmp=(String)orderid.getSelectedItem();
        String sql="select * from payinfo where cateringID=?";
        
        try {
            Connection conn=getMysqlConnection();
            PreparedStatement ps=conn.prepareStatement(sql);
            
            ps.setString(1, tmp);
            rs = ps.executeQuery();
            if(rs.next()){
                cusname.setText(rs.getString("cus_ID"));
                packid.setText(rs.getString("packageID"));
                packprice.setText(rs.getString("pack_Price"));
                plates.setText(rs.getString("plates"));
                discount.setText(rs.getString("discount"));
            }
        } catch (Exception e) {
        }
