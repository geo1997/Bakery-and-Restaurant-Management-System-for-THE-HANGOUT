    public void autoId(){
        try {
        Connection conn=getMysqlConnection();
        Statement s=conn.createStatement();
        rs=ps.executeQuery("select max(paymentID) from catpay");
        rs.next();
        rs.getString("Max(paymentID)");
        
        if(rs.getString("Max(paymentID)")==null){
            invoiceid.setText("C0001");
        }
        else
        {
            Long paymentID = Long.parseLong(rs.getString("Max(paymentID)").substring(2,rs.getString("Max(paymentID)").length()));
            paymentID++;
            invoiceid.setText("C0"+String.format("%03d", paymentID));
        }
            
        } catch (Exception e) {
        }
    }
    
