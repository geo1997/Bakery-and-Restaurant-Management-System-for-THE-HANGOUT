    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
        double pack = Double.parseDouble(packprice.getText());
        double plates = Double.parseDouble(this.plates.getText());
        double total,tax,grandtot;
        
        total = (pack+(plates*250));
        tax = (total*2)/100;
        grandtot=total+tax;
        
        String result = String.format("%.2f", grandtot);
        this.total.setText(result);
        } catch (Exception e) {
        }
        
                    try{
                Connection conn=getMysqlConnection();
                String qry="insert into catpay(paymentID,cateringID,cus_ID,pay_date,tot_amnt) values(?,?,?,?,?)";                
                PreparedStatement ps=conn.prepareStatement(qry);
                ps.setString(1,invoiceid.getText().toString());
                ps.setString(2,orderid.getSelectedItem().toString());
                ps.setString(3,cusname.getText().toString());
                ps.setString(4,date.getText().toString());                
                ps.setString(5,total.getText().toString());          
                
                int result=ps.executeUpdate();            
                if (result>=1) {
                    JOptionPane.showMessageDialog(null,result+" Order Saved Successfully to Database ....");                                
                }else{
                    JOptionPane.showMessageDialog(null,result+" Order Insertion failde Try Again..."); 
                }
                if (ps!=null) {
                    ps.close();
                }
                if (conn!=null) {
                    conn.close();
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Error in insert "+e);
            }
    }//GEN-LAST:event_jButton4ActionPerformed

