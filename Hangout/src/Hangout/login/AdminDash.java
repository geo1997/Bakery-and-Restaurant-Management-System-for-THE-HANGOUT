    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            // TODO add your handling code here:
            
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            
            
            int index = jTable1.getSelectedRow();
            
            String Table_click=(model.getValueAt(index, 0).toString());
            
            String sql = "select e.id,e.fname,e.lname,e.department,u.email,u.password from employee e, users u where e.id = u.eid and u.eid = ? ";
            Connection conn = getConnection();
            
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, Table_click);
            
             ResultSet rs = ps.executeQuery();
             
             if(rs.next()){
                    String id = rs.getString("id");
                     tfEid.setText(id);
                     String fname = rs.getString("fname");
                     tfFname.setText(fname);
                     String lname = rs.getString("lname");
                     tfLname.setText(lname);
                     
                     String dep = rs.getString("department");
                     jcombodep.setSelectedItem(dep);
                     
                     String em = rs.getString("email");
                     tfEmail.setText(em);
                     
                     String pw=rs.getString("password");
                     tfPassword.setText(pw);
                     
             }
             
            
            
            
          
        } catch (SQLException ex) {
            Logger.getLogger(AdminDash.class.getName()).log(Level.SEVERE, null, ex);
        }
            

    }//GEN-LAST:event_jTable1MouseClicked

