    public ArrayList<TransBean> getAllInvoiceData(){
        ArrayList<TransBean> al=null;
        al=new ArrayList<TransBean>();
        try{
            Connection con=getMysqlConnection();
           String qry="select * from transaction";
           Statement st=con.createStatement();
           ResultSet rs=st.executeQuery(qry);
           TransBean invoice;           
           while(rs.next()){
             invoice=new TransBean(rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4));
             al.add(invoice);

                
           }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error in getAllOrderData()"+e);
            return al;
        }
        return al;
    } 
    
    //fill the jTable1 with data
    public void fillTable(){
        ArrayList<TransBean> al=getAllInvoiceData();
        DefaultTableModel model=(DefaultTableModel)jTable2.getModel();
        model.setRowCount(0); // Empty/clear the table
        Object[] row=new Object[4];
        for (int i = 0; i < al.size(); i++) {
            row[0]=al.get(i).getCusID();
            //row[1]=al.get(i).getTot();
            row[1]=al.get(i).getDiscount();
            row[2]=al.get(i).getPoints();
            model.addRow(row);
        }                
    }


