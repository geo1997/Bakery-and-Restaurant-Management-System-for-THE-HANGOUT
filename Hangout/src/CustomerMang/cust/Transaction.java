    private void calculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateActionPerformed
        // TODO add your handling code here:
                      try {
        double total = Double.parseDouble(this.total.getText());
        double points;
        Double discount = 0.0;
        Double DBPoints=0.0;
        String id = String.valueOf(cusid.getSelectedItem());
        int eid = Integer.parseInt(id);
       PreparedStatement ps;
       ResultSet rs;
       Connection conn = getMysqlConnection();
       String sql ="select points from transaction where cusID=?";
       
       ps= conn.prepareStatement(sql);
       ps.setInt(1, eid);
       
       rs= ps.executeQuery();
       
       if(rs.next()){
           
           Double dbPoints = rs.getDouble("points");
           DBPoints=dbPoints;   
       }
        points=total/100;
        
        if(points>=500 && points<1000){
            
            discount=0.05;
            DBPoints=points+points;           
        }
        else
        if(points>=1000 && points<1500){
            discount = 0.1;
            DBPoints=points+points;
        }
        else
        if(points>=1500 && points<2000){
            
            discount=0.15;
            DBPoints=points+points;
        }
        else
        if(points>=2000 && points<5000){
            discount=0.2;
            DBPoints=points+points;
        }
        else
        if(points>=5000){
            discount=0.25;
            DBPoints=points+points;
        }
       
        //discount = 5;
        
        String result = String.format("%.2f", points);
        this.points.setText(result);
        this.discount.setText(String.valueOf(discount));
        } catch (Exception e) {
        }
    }//GEN-LAST:event_calculateActionPerformed

