    private void subActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subActionPerformed
        
         jTextArea1.setText("                                                                                                     The Hangout                       \n");
         jTextArea1.setFont(new Font("Arial Black", Font.BOLD, 10));
         
         
          jTextArea1.setText(jTextArea1.getText()+"\n                                                                                                  Customer Report "+      " \n");
             jTextArea1.setText(jTextArea1.getText()+"\n                                                                                                           2019 "+      " \n");
                   jTextArea1.setText(jTextArea1.getText()+"\n                                                                                                       September "+     "\n\n\n\n\n");          

        
                   jTextArea1.setText(jTextArea1.getText()+"\t                            Customer ID            Customer Name          Total Points   \n\n");
        
        try {
            // TODO add your handling code here:'
            String month = this.month.getSelectedItem().toString();
            String year = this.year.getSelectedItem().toString();
            
            PreparedStatement ps;
            ResultSet rs;
            Connection conn = getMysqlConnection();
            
            
            String sql= "SELECT c.cus_ID,cus.cus_Name,sum(Distinct(t.Points))as points,c.tot_amnt "
                    + "FROM catpay c inner join Customer cus on c.cus_ID=cus.customerID inner join Transaction t on cus.customerID=t.cusID "
                    + "where MONTHNAME(c.pay_date)=? and YEAR(c.pay_date)=? "
                    + "group by c.cus_ID ";
            
            ps= conn.prepareStatement(sql);
            
            ps.setString(1, month);
            ps.setString(2, year);
            
            
            rs=ps.executeQuery();
            
            
            if(rs.next())
            {
                int cus= rs.getInt("cus_ID");
                  jTextArea1.setText(jTextArea1.getText()+"\t                            "+cus+"                              ");
                  
                String cus_name=rs.getString("cus_Name");
                 jTextArea1.setText(jTextArea1.getText()+"       "+cus_name+"                  ");
                 
                 
                 Double points = rs.getDouble("points");
                  jTextArea1.setText(jTextArea1.getText()+"       "+points+"                              ");
                  
                 
                 
                
               
            }
                    
                    
                    } catch (SQLException ex) {
            Logger.getLogger(Report.class.getName()).log(Level.SEVERE, null, ex);
        }
       
       
    }//GEN-LAST:event_subActionPerformed

