     public ArrayList<ReportBean> getAllReportData(){
        ArrayList<ReportBean> al=null;
        al=new ArrayList<ReportBean>();
        try{
            Connection con=getMysqlConnection();
           String qry="select * from cus_rep";
           Statement st=con.createStatement();
           ResultSet rs=st.executeQuery(qry);
           ReportBean report;           
           while(rs.next()){
             report=new ReportBean(rs.getString(1),rs.getString(2),rs.getString(3));
             al.add(report);

                
           }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error in getAllOrderData()"+e);
            return al;
        }
        return al;
    } 
    
    //fill the jTable1 with data
    public void fillTable(){
        ArrayList<ReportBean> al=getAllReportData();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        model.setRowCount(0); // Empty/clear the table
        Object[] row=new Object[5];
        for (int i = 0; i < al.size(); i++) {
            row[0]=al.get(i).getCusID();
            row[1]=al.get(i).getCusName();
            row[2]=al.get(i).getPoints();
            model.addRow(row);
        }                
    }
   
