    public Double CalWorkHours(int id){
        
        Double WorkHours=0.0;
        try {
            Connection conn= getConnection();
            ResultSet rs;
            PreparedStatement ps;
            
            
            String sql = "Select sum(Distinct(hour(clockout)-hour(clockin))) as workHours "
                    + "from attendance a ,tempemployee temp "
                    + "where a.atd_eid=temp.teid "
                    + "and month(presentdate)=? and year(presentdate)=? "
                    + "and temp.teid=? "
                    + "and atten not in ('Absent') "
                    + "group by atd_eid ";
            
            
            ps=conn.prepareStatement(sql);
            
            ps.setInt(1, (comboBoxMonth.getMonth()+1));
            System.out.println(comboBoxMonth.getMonth()+1);
            ps.setInt(2, jYearChooser.getYear());
            System.out.println(jYearChooser.getYear());
            ps.setInt(3, id);
            System.out.println(id);
            
            rs= ps.executeQuery();
            
            if(rs.next()){
                
                
                Double workHours=rs.getDouble("workHours");
                WorkHours=workHours;
                
                
            }
          
           
            
            
        } catch (SQLException ex) {
            Logger.getLogger(EmpSal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
            return WorkHours;
      
    }
    
