      public void fillClientJtable(JTable table){
    
    String valToSearch=tfSearch.getText();
    PreparedStatement ps;
    ResultSet rs;
    String selectQuery = "Select e.id,e.fname,e.lname,te.month,te.totalSal from employee e, tempemployee te where e.id = te.teid and concat(e.id,e.fname,e.lname,te.month,te.totalSal)LIKE '%"+valToSearch+"%' order by te.month";
    
   
        try {
            
              Connection con = getConnection();
             ps=con.prepareStatement(selectQuery);
            
              rs=ps.executeQuery();
        
        DefaultTableModel tableModel = (DefaultTableModel)table.getModel();
        
        Object[] row;
       
        while(rs.next()){
            row = new Object[6];
            row[0]=rs.getInt(1);
            row[1]=rs.getString(2);
            row[2]=rs.getString(3);
            row[3]=rs.getString(4);
            row[4]=rs.getDouble(5);
         
            
            tableModel.addRow(row);
            
        }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(EmpLeaves.class.getName()).log(Level.SEVERE, null, ex);
        }
       
      

     
}
